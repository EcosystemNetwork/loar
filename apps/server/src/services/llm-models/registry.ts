export function getLlmModelById(id: string): LlmModelConfig | undefined {
  return LLM_MODELS.find((m) => m.id === id);
}

export function getEnabledLlmModels(): LlmModelConfig[] {
  return LLM_MODELS.filter((m) => m.isEnabled);
}

export function getVisibleLlmModels(): LlmModelConfig[] {
  return LLM_MODELS.filter((m) => m.isEnabled && m.isVisibleToUsers);
}

export function getLlmModelIds(): string[] {
  return LLM_MODELS.map((m) => m.id);
}
