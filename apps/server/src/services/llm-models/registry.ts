/**
 * LLM (chat/reasoning/vision) model registry.
 *
 * Covers every model that drives the canon-check, script-gen, agent
 * runtime, and worldbuilding helpers. Pricing units are USD per million
 * tokens — split into input / cached-input / output, with margin-derived
 * fiat + $LOAR rates.
 */
import type { LlmModelConfig } from './types';

export { FIAT_MARGIN, LOAR_MARGIN, LOAR_TO_USD } from '../video-models/registry';
import { FIAT_MARGIN, LOAR_MARGIN, LOAR_TO_USD } from '../video-models/registry';

function withFiatMargin(c: number): number {
  return Math.round(c * FIAT_MARGIN * 100) / 100;
}
function withLoarMargin(c: number): number {
  return Math.round(c * LOAR_MARGIN * 100) / 100;
}
function usdToCreditsPer1kTok(perMtok: number): number {
  const perThousandUsd = perMtok / 1000;
  return Math.max(1, Math.ceil(perThousandUsd / LOAR_TO_USD));
}

interface InitArgs {
  inputUsd: number;
  cachedInputUsd: number;
  outputUsd: number;
}

function pricing(args: InitArgs) {
  return {
    providerInputUsdPerMtok: args.inputUsd,
    providerCachedInputUsdPerMtok: args.cachedInputUsd,
    providerOutputUsdPerMtok: args.outputUsd,
    fiatInputUsdPerMtok: withFiatMargin(args.inputUsd),
    fiatOutputUsdPerMtok: withFiatMargin(args.outputUsd),
    loarInputUsdPerMtok: withLoarMargin(args.inputUsd),
    loarOutputUsdPerMtok: withLoarMargin(args.outputUsd),
    creditCostPer1kInputTokens: usdToCreditsPer1kTok(args.inputUsd),
    creditCostPer1kOutputTokens: usdToCreditsPer1kTok(args.outputUsd),
  };
}

