/**
 * TTS model registry — central source of truth for text-to-speech.
 *
 * Pricing convention:
 *   providerCostUsdPerMillionChars  — what the provider charges per Mchar
 *   fiatPriceUsdPerMillionChars     — providerCost × FIAT_MARGIN
 *   loarPriceUsdPerMillionChars     — providerCost × LOAR_MARGIN
 *   creditCostPer1kChars            — ceil((fiat / 1000) / LOAR_TO_USD)
 *
 * For per-second / per-token TTS (OpenAI realtime), convert to per-Mchar
 * using a 4 chars-per-token, ~120 wpm rule of thumb at integration time —
 * the realtime variants live in `realtime-voice` (future registry), not here.
 */
import type { TtsModelConfig, TtsVoicePreset } from './types';

export { FIAT_MARGIN, LOAR_MARGIN, LOAR_TO_USD } from '../video-models/registry';
import { FIAT_MARGIN, LOAR_MARGIN, LOAR_TO_USD } from '../video-models/registry';

function withFiatMargin(c: number): number {
  return Math.round(c * FIAT_MARGIN * 100) / 100;
}
function withLoarMargin(c: number): number {
  return Math.round(c * LOAR_MARGIN * 100) / 100;
}
function usdToCreditsPer1k(perMillion: number): number {
  // perMillion USD / 1000 = USD/1k chars; convert to credits at LOAR_TO_USD
  const perThousandUsd = perMillion / 1000;
  return Math.max(1, Math.ceil(perThousandUsd / LOAR_TO_USD));
}

