// ── Lookup helpers ────────────────────────────────────────────────────

export function getTtsModelById(id: string): TtsModelConfig | undefined {
  return TTS_MODELS.find((m) => m.id === id);
}

export function getEnabledTtsModels(): TtsModelConfig[] {
  return TTS_MODELS.filter((m) => m.isEnabled);
}

export function getVisibleTtsModels(): TtsModelConfig[] {
  return TTS_MODELS.filter((m) => m.isEnabled && m.isVisibleToUsers);
}

export function getTtsModelIds(): string[] {
  return TTS_MODELS.map((m) => m.id);
}

/** Quote credits to synthesize `chars` characters with a chosen model. */
export function quoteTtsCredits(modelId: string, chars: number): number {
  const m = getTtsModelById(modelId);
  if (!m) throw new Error(`Unknown TTS model: ${modelId}`);
  if (!m.isEnabled) throw new Error(`TTS model is disabled: ${modelId}`);
  const kchars = Math.max(1, Math.ceil(chars / 1000));
  return kchars * m.creditCostPer1kChars;
}
